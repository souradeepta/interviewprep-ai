    "\n"
    "def cosine_similarity(a: np.ndarray, b: np.ndarray) -> float:\n"
    "    \"\"\"Cosine similarity between two vectors.\"\"\"\n"
    "    a_norm = a / (np.linalg.norm(a) + 1e-9)\n"
    "    b_norm = b / (np.linalg.norm(b) + 1e-9)\n"
    "    return float(np.dot(a_norm, b_norm))\n"
    "\n"
    "\n"
    "def make_document_embeddings(\n"
    "    n_tokens: int, d_emb: int, rng: np.random.RandomState\n"
    ") -> np.ndarray:\n"
    "    \"\"\"\n"
    "    Simulate token embeddings for a document.\n"
    "    First and last 20% have higher signal (important content); middle is filler.\n"
    "    \"\"\"\n"
    "    emb = rng.randn(n_tokens, d_emb).astype(np.float32) * 0.1   # noise baseline\n"
    "    # Important content at start (introduction) and end (conclusion)\n"
    "    n_imp = max(1, int(n_tokens * 0.2))\n"
    "    signal = rng.randn(d_emb).astype(np.float32)\n"
    "    signal /= np.linalg.norm(signal)\n"
    "    emb[:n_imp]  += signal * 1.5    # start is important\n"
    "    emb[-n_imp:] += signal * 1.2    # end is important\n"
    "    return emb\n"
    "\n"
    "\n"
    "def truncation(emb: np.ndarray, budget: int) -> np.ndarray:\n"
    "    \"\"\"Keep the first `budget` tokens.\"\"\"\n"
    "    return emb[:budget]\n"
    "\n"
    "\n"
    "def sliding_window(emb: np.ndarray, budget: int) -> np.ndarray:\n"
    "    \"\"\"Keep a centered window of size `budget`.\"\"\"\n"
    "    n = len(emb)\n"
    "    start = max(0, n // 2 - budget // 2)\n"
    "    return emb[start:start + budget]\n"
    "\n"
    "\n"
    "def strided_sampling(emb: np.ndarray, budget: int) -> np.ndarray:\n"
    "    \"\"\"Keep every (n/budget)-th token, preserving structure.\"\"\"\n"
    "    n = len(emb)\n"
    "    if budget >= n:\n"
    "        return emb\n"
    "    stride = max(1, n // budget)\n"
    "    indices = np.arange(0, n, stride)[:budget]\n"
    "    return emb[indices]\n"
