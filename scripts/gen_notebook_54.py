    "# Simulate: retrieve 10 passages, score by relevance, compress to different budgets\n"
    "\n"
    "import re\n"
    "from collections import Counter\n"
    "\n"
    "def tokenize(text: str) -> List[str]:\n"
    "    \"\"\"Simple whitespace tokenizer.\"\"\"\n"
    "    return re.findall(r'\\w+', text.lower())\n"
    "\n"
    "\n"
    "def bm25_score(\n"
    "    query: str,\n"
    "    passage: str,\n"
    "    k1: float = 1.5,\n"
    "    b: float = 0.75,\n"
    "    avg_doc_len: float = 30.0,\n"
    ") -> float:\n"
    "    \"\"\"BM25 relevance score of passage for given query.\"\"\"\n"
    "    q_tokens = set(tokenize(query))\n"
    "    p_tokens = tokenize(passage)\n"
    "    p_len = len(p_tokens)\n"
    "    p_freq = Counter(p_tokens)\n"
    "    score = 0.0\n"
    "    for term in q_tokens:\n"
    "        tf = p_freq.get(term, 0)\n"
    "        idf = np.log(1 + 1 / (tf + 1e-9))   # Simplified IDF\n"
    "        tf_norm = tf * (k1 + 1) / (tf + k1 * (1 - b + b * p_len / avg_doc_len))\n"
