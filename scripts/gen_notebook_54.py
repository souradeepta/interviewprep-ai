# Cell 12: RW Example 3 code (Code) ~80 lines
cells.append(nbformat.v4.new_code_cell(
    "# Real-World Example 3: Hierarchical compression — verbatim recent + compressed old\n"
    "# Old context -> N summary embeddings; recent context -> kept verbatim\n"
    "\n"
    "class HierarchicalContextManager:\n"
    "    \"\"\"\n"
    "    Maintains a context with a fixed window:\n"
    "    - Last `verbatim_window` tokens: kept as-is (high fidelity)\n"
    "    - Older tokens: compressed into `n_summary` summary embeddings\n"
    "    This mimics RMT (Recurrent Memory Transformer) memory tokens.\n"
    "    \"\"\"\n"
    "    def __init__(\n"
    "        self,\n"
    "        max_total_tokens: int,\n"
    "        verbatim_window: int,\n"
    "        n_summary: int,\n"
    "        d_model: int,\n"
    "    ):\n"
    "        self.max_total = max_total_tokens\n"
    "        self.verbatim = verbatim_window\n"
    "        self.n_summary = n_summary\n"
    "        self.d_model = d_model\n"
    "        self.history: List[np.ndarray] = []   # All token embeddings seen so far\n"
    "        self.n_compressed = 0\n"
    "\n"
    "    def add_tokens(self, new_tokens: np.ndarray):\n"
    "        \"\"\"Add new tokens; compress old context if over budget.\"\"\"\n"
    "        for tok in new_tokens:\n"
    "            self.history.append(tok)\n"
    "\n"
    "    def get_context(self) -> Tuple[np.ndarray, int]:\n"
    "        \"\"\"\n"
    "        Returns (effective_context, total_token_count) where effective_context\n"
    "        is summary embeddings + verbatim recent tokens.\n"
    "        \"\"\"\n"
    "        n = len(self.history)\n"
    "        if n <= self.max_total:\n"
    "            return np.stack(self.history), n\n"
    "\n"
    "        # Split: old context (to compress) and recent (verbatim)\n"
    "        verbatim_start = n - self.verbatim\n"
    "        old_tokens  = np.stack(self.history[:verbatim_start])   # (old_len, D)\n"
    "        recent_tokens = np.stack(self.history[verbatim_start:])   # (verbatim, D)\n"
    "\n"
    "        # Compress old tokens into n_summary mean-pooled summaries\n"
    "        chunk_size = max(1, len(old_tokens) // self.n_summary)\n"
    "        summaries = []\n"
    "        for i in range(self.n_summary):\n"
    "            start = i * chunk_size\n"
    "            end   = min(start + chunk_size, len(old_tokens))\n"
    "            if start >= len(old_tokens):\n"
    "                summaries.append(np.zeros(self.d_model))\n"
    "            else:\n"
    "                summaries.append(old_tokens[start:end].mean(axis=0))\n"
    "        summary_embs = np.stack(summaries)           # (n_summary, D)\n"
    "        self.n_compressed = len(old_tokens)\n"
    "\n"
    "        # Concatenate: summaries + verbatim recent\n"
    "        effective = np.concatenate([summary_embs, recent_tokens], axis=0)\n"
    "        return effective, n\n"
    "\n"
    "\n"
    "def context_quality(\n"
    "    context: np.ndarray, full_history: List[np.ndarray]\n"
    ") -> float:\n"
    "    \"\"\"Cosine similarity of context mean to full history mean.\"\"\"\n"
    "    ctx_mean  = context.mean(axis=0)\n"
    "    full_mean = np.stack(full_history).mean(axis=0)\n"
    "    return cosine_similarity(ctx_mean, full_mean)\n"
    "\n"
    "\n"
    "rng_hier = np.random.RandomState(55)\n"
    "D_H = 128\n"
    "\n"
    "# Simulate a long conversation: 800 tokens generated over time\n"
    "N_STREAM = 800\n"
    "stream_tokens = [rng_hier.randn(D_H).astype(np.float32) for _ in range(N_STREAM)]\n"
    "# Add 'key facts' at positions 100, 300, 600 (important info across the conversation)\n"
    "key_signal = rng_hier.randn(D_H).astype(np.float32)\n"
    "for pos in [100, 300, 600]:\n"
    "    stream_tokens[pos] = key_signal * 2.0\n"
    "\n"
    "# Compare three strategies at token 800\n"
    "strategies_hier = {\n"
    "    'Truncation (last 256)':   {'verbatim': 256, 'n_summary': 0,  'total': 256},\n"
    "    'Hierarchical (16+240)':   {'verbatim': 240, 'n_summary': 16, 'total': 256},\n"
    "    'Hierarchical (32+224)':   {'verbatim': 224, 'n_summary': 32, 'total': 256},\n"
    "    'Hierarchical (64+192)':   {'verbatim': 192, 'n_summary': 64, 'total': 256},\n"
    "}\n"
    "\n"
    "print(f'Stream: {N_STREAM} tokens, context budget: 256 tokens')\n"
    "print(f'{\"Strategy\":30s} {\"QualitySim\":12s} {\"ComprRatio\":12s} {\"SummaryCount\":12s}')\n"
    "print('-' * 68)\n"
    "\n"
    "for name, cfg in strategies_hier.items():\n"
    "    mgr = HierarchicalContextManager(\n"
    "        max_total_tokens=cfg['total'],\n"
    "        verbatim_window=cfg['verbatim'],\n"
    "        n_summary=cfg['n_summary'],\n"
    "        d_model=D_H,\n"
    "    )\n"
    "    mgr.add_tokens(np.stack(stream_tokens))\n"
    "    ctx, total = mgr.get_context()\n"
    "    quality = context_quality(ctx, stream_tokens)\n"
    "    ratio   = N_STREAM / cfg['total']\n"
    "    print(f'{name:30s} {quality:<12.4f} {ratio:<12.1f}x {cfg[\"n_summary\"]:<12d}')\n"
    "\n"
    "print('\\nHierarchical compression with 32 summary tokens captures old context better than truncation.')\n"
    "print('Key positions at 100, 300 (outside verbatim window) contribute to summary embeddings.')\n"
))

