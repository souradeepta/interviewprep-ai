    "        teacher_model, target_class=cls,\n"
    "        d_in=IN_F, n_samples=N_PSEUDO_PER_CLASS, n_steps=80,\n"
    "    )\n"
    "    pseudo_X_list.append(pseudo)\n"
    "    pseudo_y_list.append(torch.full((N_PSEUDO_PER_CLASS,), cls, dtype=torch.long, device=device))\n"
    "\n"
    "pseudo_X = torch.cat(pseudo_X_list, dim=0)\n"
    "pseudo_y = torch.cat(pseudo_y_list, dim=0)\n"
    "print(f'Pseudo dataset: {pseudo_X.shape} (teacher confidence check follows)')\n"
    "\n"
    "# Verify teacher confidence on pseudo-data (should be high for generated class)\n"
    "teacher_model.eval()\n"
