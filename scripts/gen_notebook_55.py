# Cell 12: RW Example 3 code (Code) ~80 lines
cells.append(nbformat.v4.new_code_cell(
    "# Real-World Example 3: Continual distillation — new task + old task retention\n"
    "# Old teacher provides soft labels for old task; EWC-like penalty on shared parameters\n"
    "\n"
    "def train_task(\n"
    "    model: nn.Module,\n"
    "    X_tr: torch.Tensor, y_tr: torch.Tensor,\n"
    "    epochs: int = 60, lr: float = 0.005\n"
    ") -> nn.Module:\n"
    "    \"\"\"Train model on a task with hard labels.\"\"\"\n"
    "    opt = torch.optim.Adam(model.parameters(), lr=lr)\n"
    "    model.train()\n"
    "    for ep in range(epochs):\n"
    "        idx = torch.randperm(len(X_tr))\n"
    "        for start in range(0, len(X_tr), 64):\n"
    "            b = idx[start:start+64]\n"
    "            loss = F.cross_entropy(model(X_tr[b]), y_tr[b])\n"
    "            opt.zero_grad(); loss.backward(); opt.step()\n"
    "    return model\n"
    "\n"
    "\n"
    "def compute_fisher(\n"
    "    model: nn.Module, X: torch.Tensor, y: torch.Tensor\n"
    ") -> Dict[str, torch.Tensor]:\n"
    "    \"\"\"Approximate Fisher information (parameter importance) for EWC.\"\"\"\n"
    "    fisher = {n: torch.zeros_like(p) for n, p in model.named_parameters()}\n"
    "    model.eval()\n"
    "    for xi, yi in zip(X[:100], y[:100]):   # Sample 100 points\n"
    "        model.zero_grad()\n"
    "        logit = model(xi.unsqueeze(0))\n"
    "        loss = F.cross_entropy(logit, yi.unsqueeze(0))\n"
    "        loss.backward()\n"
    "        for n, p in model.named_parameters():\n"
    "            if p.grad is not None:\n"
    "                fisher[n] += p.grad.data.pow(2)\n"
    "    for n in fisher:\n"
    "        fisher[n] /= 100\n"
    "    return fisher\n"
    "\n"
    "\n"
    "# Task A: 4-class classification (first task)\n"
    "IN_F_CL = 16\n"
    "N_CLS_A, N_CLS_B = 4, 4\n"
    "N_CL = 400\n"
    "\n"
    "X_A = torch.randn(N_CL, IN_F_CL)\n"
    "y_A = torch.randint(0, N_CLS_A, (N_CL,))\n"
    "X_A[y_A == 0, :2] += 1.5; X_A[y_A == 1, :2] -= 1.5\n"
    "X_A[y_A == 2, 2:4] += 1.5; X_A[y_A == 3, 2:4] -= 1.5\n"
    "\n"
    "# Task B: different 4-class classification (second task)\n"
    "X_B = torch.randn(N_CL, IN_F_CL)\n"
    "y_B = torch.randint(0, N_CLS_B, (N_CL,))\n"
    "X_B[y_B == 0, 4:6] += 1.5; X_B[y_B == 1, 4:6] -= 1.5\n"
    "X_B[y_B == 2, 6:8] += 1.5; X_B[y_B == 3, 6:8] -= 1.5\n"
    "\n"
    "X_A, y_A = X_A.to(device), y_A.to(device)\n"
    "X_B, y_B = X_B.to(device), y_B.to(device)\n"
    "\n"
    "# Step 1: Train teacher on Task A\n"
    "torch.manual_seed(42)\n"
    "teacher_A = StudentNet(IN_F_CL, 64, N_CLS_A).to(device)\n"
    "teacher_A = train_task(teacher_A, X_A, y_A, epochs=60)\n"
    "old_params = {n: p.clone().detach() for n, p in teacher_A.named_parameters()}\n"
    "fisher_A = compute_fisher(teacher_A, X_A, y_A)\n"
    "\n"
    "acc_A_before = (teacher_A(X_A).argmax(1) == y_A).float().mean().item()\n"
    "print(f'Teacher A (Task A) acc before Task B training: {acc_A_before:.3f}')\n"
    "\n"
    "# Step 2: Continual distillation — fine-tune teacher on Task B with EWC penalty\n"
    "EWC_LAMBDA = 100.0\n"
    "opt_cont = torch.optim.Adam(teacher_A.parameters(), lr=0.003)\n"
    "\n"
    "for ep in range(60):\n"
    "    idx = torch.randperm(N_CL)\n"
    "    for start in range(0, N_CL, 64):\n"
    "        b = idx[start:start+64]\n"
    "        xb, yb = X_B[b], y_B[b]\n"
    "        logits_B = teacher_A(xb)   # Now learning Task B\n"
    "        task_loss = F.cross_entropy(logits_B, yb)\n"
    "        # EWC penalty: preserve important parameters for Task A\n"
    "        ewc_loss = torch.tensor(0.0, device=device)\n"
    "        for n, p in teacher_A.named_parameters():\n"
    "            ewc_loss += (fisher_A[n] * (p - old_params[n]).pow(2)).sum()\n"
    "        loss = task_loss + (EWC_LAMBDA / 2) * ewc_loss\n"
    "        opt_cont.zero_grad(); loss.backward(); opt_cont.step()\n"
    "\n"
    "acc_A_after = (teacher_A(X_A).argmax(1) == y_A).float().mean().item()\n"
    "acc_B_after = (teacher_A(X_B).argmax(1) == y_B).float().mean().item()\n"
    "\n"
    "print(f'After continual training on Task B:')\n"
    "print(f'  Task A retention (backward transfer): {acc_A_after:.3f} (was {acc_A_before:.3f})')\n"
    "print(f'  Task B accuracy (forward transfer):   {acc_B_after:.3f}')\n"
    "print(f'  Forgetting rate: {acc_A_before - acc_A_after:.3f}')\n"
    "print(f'\\nEWC penalty (lambda={EWC_LAMBDA}) preserves Task A knowledge while learning Task B.')\n"
    "print(f'Without EWC, catastrophic forgetting would reduce Task A acc to near-random.')\n"
))

