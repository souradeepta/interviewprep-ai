    "\n"
    "for ep in range(80):\n"
    "    idx = torch.randperm(len(pseudo_X))\n"
    "    for start in range(0, len(pseudo_X), 32):\n"
    "        b = idx[start:start+32]\n"
    "        xb, yb = pseudo_X[b], pseudo_y[b]\n"
    "        with torch.no_grad():\n"
    "            t_logits = teacher_model(xb)\n"
    "        s_logits = student_df(xb)\n"
