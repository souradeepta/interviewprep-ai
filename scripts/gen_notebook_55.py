    "# Level 2: Online mutual learning — DML + EMA teacher variant\n"
    "\n"
    "class StudentNet(nn.Module):\n"
    "    \"\"\"Small student network for KD experiments.\"\"\"\n"
    "    def __init__(self, in_features: int, hidden: int, n_classes: int):\n"
    "        super().__init__()\n"
    "        self.net = nn.Sequential(\n"
    "            nn.Linear(in_features, hidden), nn.LayerNorm(hidden), nn.ReLU(),\n"
    "            nn.Linear(hidden, hidden), nn.ReLU(),\n"
    "            nn.Linear(hidden, n_classes),\n"
    "        )\n"
    "\n"
    "    def forward(self, x: torch.Tensor) -> torch.Tensor:\n"
    "        return self.net(x)\n"
    "\n"
    "\n"
    "def kd_loss(\n"
    "    student_logits: torch.Tensor,\n"
    "    teacher_logits: torch.Tensor,\n"
    "    hard_labels: torch.Tensor,\n"
    "    T: float = 4.0,\n"
    "    alpha: float = 0.5,\n"
    ") -> Tuple[torch.Tensor, torch.Tensor, torch.Tensor]:\n"
    "    \"\"\"Returns (total_loss, ce_loss, kl_loss). T^2 scaling applied to KL term.\"\"\"\n"
    "    ce = F.cross_entropy(student_logits, hard_labels)\n"
    "    p_teacher = F.softmax(teacher_logits / T, dim=-1)\n"
    "    p_student = F.log_softmax(student_logits / T, dim=-1)\n"
    "    # KL divergence: T^2 scaling restores gradient scale at high temperature\n"
    "    kl = F.kl_div(p_student, p_teacher, reduction='batchmean') * (T ** 2)\n"
    "    total = (1 - alpha) * ce + alpha * kl\n"
    "    return total, ce, kl\n"
    "\n"
    "\n"
    "def update_ema_teacher(\n"
    "    teacher: nn.Module, student: nn.Module, beta: float = 0.999\n"
    ") -> None:\n"
    "    \"\"\"EMA teacher update: teacher = beta * teacher + (1 - beta) * student.\"\"\"\n"
    "    with torch.no_grad():\n"
    "        for t_param, s_param in zip(teacher.parameters(), student.parameters()):\n"
    "            t_param.data = beta * t_param.data + (1 - beta) * s_param.data\n"
    "\n"
    "\n"
    "# Synthetic torch dataset\n"
    "IN_F = 16\n"
    "N_CLS_T = 6\n"
    "HIDDEN = 64\n"
    "N_SAMP = 600\n"
    "EPOCHS = 80\n"
    "BATCH = 64\n"
    "T_TEMP_T = 4.0\n"
    "ALPHA_T = 0.4\n"
    "\n"
    "X_torch = torch.randn(N_SAMP, IN_F)\n"
    "y_torch = torch.randint(0, N_CLS_T, (N_SAMP,))\n"
    "for c in range(N_CLS_T):\n"
    "    X_torch[y_torch == c, :3] += (c - 2.5) * 1.2\n"
    "\n"
    "tr_n = int(0.75 * N_SAMP)\n"
    "X_tr_t2 = X_torch[:tr_n].to(device)\n"
    "y_tr_t2 = y_torch[:tr_n].to(device)\n"
    "X_val_t2 = X_torch[tr_n:].to(device)\n"
    "y_val_t2 = y_torch[tr_n:].to(device)\n"
    "\n"
    "\n"
    "def run_training(\n"
    "    mode: str,  # 'no_kd', 'offline_kd', 'online_dml', 'ema_teacher'\n"
    "    X_tr, y_tr, X_val, y_val, teacher_net=None,\n"
    ") -> Tuple[List[float], List[float], float]:\n"
    "    \"\"\"Train student with specified mode. Returns (train_losses, val_accs, final_acc).\"\"\"\n"
    "    student = StudentNet(IN_F, HIDDEN, N_CLS_T).to(device)\n"
    "    opt = torch.optim.Adam(student.parameters(), lr=0.005)\n"
    "\n"
    "    if mode == 'online_dml':\n"
    "        peer = StudentNet(IN_F, HIDDEN, N_CLS_T).to(device)\n"
    "        opt_peer = torch.optim.Adam(peer.parameters(), lr=0.005)\n"
    "    elif mode == 'ema_teacher':\n"
    "        ema_teacher = copy.deepcopy(student)\n"
    "        for p in ema_teacher.parameters():\n"
    "            p.requires_grad = False\n"
    "\n"
    "    train_losses, val_accs = [], []\n"
    "    n = len(X_tr)\n"
    "\n"
    "    try:\n"
    "        for ep in range(EPOCHS):\n"
    "            # Mini-batch training\n"
    "            idx = torch.randperm(n)\n"
    "            ep_loss = 0.0\n"
    "            for start in range(0, n, BATCH):\n"
    "                b_idx = idx[start:start + BATCH]\n"
    "                xb, yb = X_tr[b_idx], y_tr[b_idx]\n"
    "                s_logits = student(xb)\n"
    "\n"
    "                if mode == 'no_kd':\n"
    "                    loss = F.cross_entropy(s_logits, yb)\n"
    "                elif mode == 'offline_kd' and teacher_net is not None:\n"
    "                    with torch.no_grad():\n"
    "                        t_logits = teacher_net(xb)\n"
    "                    loss, _, _ = kd_loss(s_logits, t_logits, yb, T_TEMP_T, ALPHA_T)\n"
    "                elif mode == 'online_dml':\n"
    "                    peer_logits = peer(xb)\n"
    "                    loss, _, _ = kd_loss(s_logits, peer_logits.detach(), yb, T_TEMP_T, ALPHA_T)\n"
    "                    # Peer learns from student\n"
    "                    peer_loss, _, _ = kd_loss(peer_logits, s_logits.detach(), yb, T_TEMP_T, ALPHA_T)\n"
    "                    opt_peer.zero_grad()\n"
    "                    peer_loss.backward()\n"
    "                    opt_peer.step()\n"
    "                elif mode == 'ema_teacher':\n"
    "                    with torch.no_grad():\n"
    "                        t_logits = ema_teacher(xb)\n"
    "                    loss, _, _ = kd_loss(s_logits, t_logits, yb, T_TEMP_T, ALPHA_T)\n"
    "                    update_ema_teacher(ema_teacher, student, beta=0.999)\n"
    "                else:\n"
    "                    loss = F.cross_entropy(s_logits, yb)\n"
    "\n"
    "                opt.zero_grad()\n"
    "                loss.backward()\n"
    "                opt.step()\n"
    "                ep_loss += loss.item()\n"
    "\n"
    "            train_losses.append(ep_loss / (n // BATCH))\n"
    "            with torch.no_grad():\n"
    "                val_acc = (student(X_val).argmax(1) == y_val).float().mean().item()\n"
    "            val_accs.append(val_acc)\n"
    "    except RuntimeError as e:\n"
    "        if 'out of memory' in str(e).lower():\n"
    "            print(f'OOM in {mode}: reduce BATCH or HIDDEN and retry')\n"
    "            raise\n"
    "        raise\n"
    "\n"
    "    return train_losses, val_accs, val_accs[-1]\n"
    "\n"
    "\n"
    "# First train a teacher\n"
    "teacher_model = StudentNet(IN_F, HIDDEN * 2, N_CLS_T).to(device)  # Larger teacher\n"
    "opt_t = torch.optim.Adam(teacher_model.parameters(), lr=0.005)\n"
    "for ep in range(EPOCHS):\n"
    "    idx = torch.randperm(tr_n)\n"
    "    for start in range(0, tr_n, BATCH):\n"
    "        b = idx[start:start + BATCH]\n"
    "        loss = F.cross_entropy(teacher_model(X_tr_t2[b]), y_tr_t2[b])\n"
    "        opt_t.zero_grad(); loss.backward(); opt_t.step()\n"
    "with torch.no_grad():\n"
    "    teach_final_acc = (teacher_model(X_val_t2).argmax(1) == y_val_t2).float().mean().item()\n"
    "print(f'Teacher val_acc: {teach_final_acc:.3f}')\n"
    "\n"
    "# Run all modes\n"
    "results_kd = {}\n"
    "for mode in ['no_kd', 'offline_kd', 'online_dml', 'ema_teacher']:\n"
    "    torch.manual_seed(42)\n"
    "    tl, va, final = run_training(mode, X_tr_t2, y_tr_t2, X_val_t2, y_val_t2,\n"
