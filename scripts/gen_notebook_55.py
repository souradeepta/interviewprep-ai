    "# Level 1: Offline KD — numpy toy example\n"
    "# Loss = (1-alpha)*CE(student, hard_labels) + alpha * T^2 * KL(student_T || teacher_T)\n"
    "\n"
    "def softmax_temp(logits: np.ndarray, T: float) -> np.ndarray:\n"
    "    \"\"\"Temperature-scaled softmax.\"\"\"\n"
    "    logits_T = logits / T\n"
    "    logits_T -= logits_T.max(axis=-1, keepdims=True)\n"
    "    e = np.exp(logits_T)\n"
    "    return e / e.sum(axis=-1, keepdims=True)\n"
    "\n"
    "\n"
    "def kl_divergence(p: np.ndarray, q: np.ndarray, eps: float = 1e-9) -> float:\n"
    "    \"\"\"KL(p || q) — mean over batch.\"\"\"\n"
    "    return float(np.mean(np.sum(p * (np.log(p + eps) - np.log(q + eps)), axis=-1)))\n"
    "\n"
    "\n"
    "def ce_loss(logits: np.ndarray, labels: np.ndarray) -> float:\n"
    "    \"\"\"Cross-entropy loss (mean over batch).\"\"\"\n"
    "    probs = softmax_temp(logits, T=1.0)\n"
    "    batch_idx = np.arange(len(labels))\n"
    "    return float(-np.mean(np.log(probs[batch_idx, labels] + 1e-9)))\n"
    "\n"
    "\n"
    "def distillation_loss(\n"
    "    student_logits: np.ndarray,\n"
    "    teacher_logits: np.ndarray,\n"
    "    hard_labels: np.ndarray,\n"
    "    T: float = 4.0,\n"
    "    alpha: float = 0.5,\n"
    ") -> Tuple[float, float, float]:\n"
    "    \"\"\"Returns (total_loss, ce_loss_val, kl_loss_val).\"\"\"\n"
    "    hard_loss = ce_loss(student_logits, hard_labels)\n"
    "    # Temperature-scaled soft labels from teacher\n"
    "    p_teacher = softmax_temp(teacher_logits, T)\n"
    "    p_student = softmax_temp(student_logits, T)\n"
    "    # T^2 rescaling factor restores gradient magnitude\n"
    "    soft_loss = T ** 2 * kl_divergence(p_student, p_teacher)\n"
    "    total = (1 - alpha) * hard_loss + alpha * soft_loss\n"
    "    return total, hard_loss, soft_loss\n"
    "\n"
