    "# Real-World Example 2: Data-free KD — generate pseudo-data to maximize teacher activations\n"
    "# Inspired by DeepInversion / ZSKD: optimize random noise to look like natural data for the teacher\n"
    "\n"
    "def generate_pseudo_data(\n"
    "    teacher: nn.Module,\n"
    "    target_class: int,\n"
    "    d_in: int,\n"
    "    n_samples: int = 32,\n"
    "    n_steps: int = 100,\n"
    "    lr: float = 0.05,\n"
    ") -> torch.Tensor:\n"
    "    \"\"\"\n"
    "    Optimize random noise to maximize teacher's softmax probability for target_class.\n"
    "    Returns optimized pseudo-samples of shape (n_samples, d_in).\n"
    "    \"\"\"\n"
    "    teacher.eval()\n"
    "    # Start from random noise\n"
    "    x_pseudo = nn.Parameter(torch.randn(n_samples, d_in, device=device) * 0.1)\n"
    "    opt_pseudo = torch.optim.Adam([x_pseudo], lr=lr)\n"
    "\n"
    "    for step in range(n_steps):\n"
    "        teacher_logits = teacher(x_pseudo)\n"
    "        # Maximize probability for target class (minimize negative log-prob)\n"
    "        loss = F.cross_entropy(\n"
    "            teacher_logits,\n"
    "            torch.full((n_samples,), target_class, dtype=torch.long, device=device)\n"
    "        )\n"
    "        # Regularization: keep pseudo-data close to unit Gaussian (prevents degenerate values)\n"
    "        loss += 0.01 * x_pseudo.norm()\n"
    "        opt_pseudo.zero_grad()\n"
    "        loss.backward()\n"
    "        opt_pseudo.step()\n"
    "\n"
    "    return x_pseudo.detach()\n"
    "\n"
    "\n"
    "# Generate pseudo-data for each class\n"
    "N_CLS_DF = N_CLS_T\n"
    "N_PSEUDO_PER_CLASS = 40\n"
    "\n"
    "print(f'Generating pseudo-data ({N_PSEUDO_PER_CLASS} samples/class x {N_CLS_DF} classes)...')\n"
    "pseudo_X_list, pseudo_y_list = [], []\n"
    "\n"
    "for cls in range(N_CLS_DF):\n"
