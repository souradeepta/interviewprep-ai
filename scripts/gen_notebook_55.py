cells.append(nbformat.v4.new_code_cell(
    "# Real-World Example 1: Self-distillation — final layer teaches intermediate layers\n"
    "# Born-Again Networks: student = teacher after one distillation generation\n"
    "\n"
    "class SelfDistillNet(nn.Module):\n"
    "    \"\"\"\n"
    "    12-layer network with exit heads at layers 4, 8, 12.\n"
    "    The final-layer head serves as the 'teacher' for intermediate heads.\n"
    "    \"\"\"\n"
    "    def __init__(self, d_in: int, n_classes: int, n_layers: int = 12):\n"
    "        super().__init__()\n"
    "        self.layers = nn.ModuleList([\n"
    "            nn.Sequential(nn.Linear(d_in, d_in), nn.LayerNorm(d_in), nn.GELU())\n"
    "            for _ in range(n_layers)\n"
    "        ])\n"
    "        # Exit heads at layers 4, 8, 12 (0-indexed: 3, 7, 11)\n"
    "        self.exit_heads = nn.ModuleList([\n"
    "            nn.Linear(d_in, n_classes) for _ in range(3)\n"
    "        ])\n"
    "        self.exit_positions = [3, 7, 11]\n"
    "\n"
    "    def forward_all_exits(\n"
    "        self, x: torch.Tensor\n"
    "    ) -> List[torch.Tensor]:\n"
    "        \"\"\"Returns list of logits at each exit head [layer4, layer8, layer12].\"\"\"\n"
    "        h = x\n"
    "        exit_logits = []\n"
    "        exit_idx = 0\n"
    "        for layer_i in range(len(self.layers)):\n"
    "            h = self.layers[layer_i](h)\n"
    "            if exit_idx < len(self.exit_positions) and layer_i == self.exit_positions[exit_idx]:\n"
    "                exit_logits.append(self.exit_heads[exit_idx](h))\n"
    "                exit_idx += 1\n"
    "        return exit_logits  # [L4_logits, L8_logits, L12_logits]\n"
    "\n"
    "\n"
    "D_SD = 64\n"
    "N_CLS_SD = 6\n"
    "N_SAMP_SD = 500\n"
    "EPOCHS_SD = 60\n"
    "\n"
    "X_sd = torch.randn(N_SAMP_SD, D_SD)\n"
    "y_sd = torch.randint(0, N_CLS_SD, (N_SAMP_SD,))\n"
    "for c in range(N_CLS_SD):\n"
    "    X_sd[y_sd == c, :4] += (c - 2.5) * 0.8\n"
    "X_sd, y_sd = X_sd.to(device), y_sd.to(device)\n"
    "\n"
    "# Train WITHOUT self-distillation (each exit head trained on hard labels independently)\n"
    "net_no_sd = SelfDistillNet(D_SD, N_CLS_SD).to(device)\n"
    "opt_no_sd = torch.optim.Adam(net_no_sd.parameters(), lr=0.003)\n"
    "for ep in range(EPOCHS_SD):\n"
    "    for start in range(0, N_SAMP_SD, 64):\n"
    "        xb, yb = X_sd[start:start+64], y_sd[start:start+64]\n"
    "        all_logits = net_no_sd.forward_all_exits(xb)\n"
    "        loss = sum(F.cross_entropy(lg, yb) for lg in all_logits) / len(all_logits)\n"
    "        opt_no_sd.zero_grad(); loss.backward(); opt_no_sd.step()\n"
    "\n"
    "# Train WITH self-distillation (final head teaches intermediate heads)\n"
    "net_sd = SelfDistillNet(D_SD, N_CLS_SD).to(device)\n"
    "opt_sd = torch.optim.Adam(net_sd.parameters(), lr=0.003)\n"
    "T_SD = 4.0\n"
    "ALPHA_SD = 0.5\n"
    "sd_curves = {'L4': [], 'L8': [], 'L12': []}\n"
    "\n"
    "for ep in range(EPOCHS_SD):\n"
    "    for start in range(0, N_SAMP_SD, 64):\n"
    "        xb, yb = X_sd[start:start+64], y_sd[start:start+64]\n"
    "        all_logits = net_sd.forward_all_exits(xb)   # [L4, L8, L12]\n"
    "        final_logits = all_logits[-1]   # L12 is the 'teacher'\n"
    "        # L4 and L8 distill from L12 (final layer)\n"
    "        loss = F.cross_entropy(final_logits, yb)   # Final layer: hard labels only\n"
    "        for i, lg in enumerate(all_logits[:-1]):\n"
    "            ce_part = F.cross_entropy(lg, yb)\n"
    "            kl_part = F.kl_div(\n"
    "                F.log_softmax(lg / T_SD, dim=-1),\n"
    "                F.softmax(final_logits.detach() / T_SD, dim=-1),\n"
    "                reduction='batchmean',\n"
    "            ) * (T_SD ** 2)\n"
    "            loss += (1 - ALPHA_SD) * ce_part + ALPHA_SD * kl_part\n"
    "        opt_sd.zero_grad(); loss.backward(); opt_sd.step()\n"
    "\n"
    "# Compare accuracy at each exit head\n"
    "net_no_sd.eval(); net_sd.eval()\n"
    "with torch.no_grad():\n"
    "    logits_no = net_no_sd.forward_all_exits(X_sd)\n"
    "    logits_sd = net_sd.forward_all_exits(X_sd)\n"
    "\n"
    "exit_names = ['Layer 4', 'Layer 8', 'Layer 12']\n"
    "print(f'{\"Exit\":12s} {\"No SD Acc\":12s} {\"With SD Acc\":12s} {\"Improvement\":12s}')\n"
    "print('-' * 50)\n"
    "for name, lg_no, lg_sd in zip(exit_names, logits_no, logits_sd):\n"
    "    acc_no = (lg_no.argmax(1) == y_sd).float().mean().item()\n"
    "    acc_sd = (lg_sd.argmax(1) == y_sd).float().mean().item()\n"
    "    print(f'{name:12s} {acc_no:<12.3f} {acc_sd:<12.3f} {acc_sd-acc_no:+.3f}')\n"
    "\n"
    "print('\\nSelf-distillation improves intermediate heads; final head unaffected (trained on hard labels).')\n"
    "print('This enables early exit with higher accuracy than training heads independently.')\n"
))

