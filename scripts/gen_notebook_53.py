    "# Real-World Example 1: Conditional FFN skip gate in transformer block\n"
    "# Gate = sigmoid(linear(attn_output)); skip FFN if gate < threshold\n"
    "\n"
    "class ConditionalTransformerBlock(nn.Module):\n"
    "    \"\"\"Transformer block with conditional FFN skip based on attention confidence.\"\"\"\n"
    "    def __init__(self, d_model: int, n_heads: int, d_ff: int):\n"
    "        super().__init__()\n"
    "        self.attn = nn.MultiheadAttention(d_model, n_heads, batch_first=True)\n"
    "        self.norm1 = nn.LayerNorm(d_model)\n"
    "        self.norm2 = nn.LayerNorm(d_model)\n"
    "        self.ffn = nn.Sequential(\n"
    "            nn.Linear(d_model, d_ff), nn.GELU(), nn.Linear(d_ff, d_model)\n"
    "        )\n"
    "        # Gate network: linear -> sigmoid; decides per-token FFN skip\n"
    "        self.gate = nn.Sequential(\n"
    "            nn.Linear(d_model, 1),\n"
    "            nn.Sigmoid()\n"
    "        )\n"
    "\n"
    "    def forward(\n"
    "        self, x: torch.Tensor, ffn_threshold: float = 0.5\n"
    "    ) -> Tuple[torch.Tensor, float, int]:\n"
    "        \"\"\"Returns (output, skip_fraction, n_skipped).\"\"\"\n"
    "        # Attention sublayer\n"
    "        attn_out, _ = self.attn(x, x, x)\n"
    "        h = self.norm1(x + attn_out)\n"
    "\n"
    "        # Gate decision per token (flatten batch*seq for gate)\n"
    "        B, S, D = h.shape\n"
    "        h_flat = h.view(B * S, D)\n"
    "        gate_vals = self.gate(h_flat).squeeze(-1)       # (B*S,)\n"
    "        execute_mask = gate_vals >= ffn_threshold        # True = run FFN\n"
    "        skip_fraction = (~execute_mask).float().mean().item()\n"
    "        n_skipped = int((~execute_mask).sum().item())\n"
    "\n"
    "        # Conditional FFN application\n"
    "        ffn_out = h_flat.clone()\n"
    "        if execute_mask.any():\n"
    "            ffn_out[execute_mask] = (\n"
    "                gate_vals[execute_mask, None] * self.ffn(h_flat[execute_mask]) +\n"
    "                (1 - gate_vals[execute_mask, None]) * h_flat[execute_mask]\n"
    "            )\n"
    "\n"
    "        output = self.norm2(h + ffn_out.view(B, S, D))\n"
    "        return output, skip_fraction, n_skipped\n"
    "\n"
    "\n"
    "# Test on a sequence batch\n"
