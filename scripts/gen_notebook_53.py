    "            last_logits = self.exit_heads[-1](h[still_in])\n"
    "            last_preds  = last_logits.argmax(dim=-1)\n"
    "            final_preds[still_in] = last_preds\n"
    "            for b in range(B):\n"
    "                if still_in[b]:\n"
    "                    exit_layers[b] = self.n_layers\n"
    "\n"
    "        return final_preds, final_conf, exit_layers\n"
    "\n"
    "\n"
