    "# Real-World Example 3: Input-conditioned routing — specialist experts\n"
    "# Token type 0 = 'numeric' (high-magnitude, sparse), type 1 = 'textual' (dense, lower magnitude)\n"
    "\n"
    "class SpecialistMoE(nn.Module):\n"
    "    \"\"\"MoE with 2 specialist experts + learned router. Expects input with type labels.\"\"\"\n"
    "    def __init__(self, d_model: int, d_ff: int):\n"
    "        super().__init__()\n"
    "        self.numeric_expert  = ExpertFFN(d_model, d_ff)\n"
    "        self.semantic_expert = ExpertFFN(d_model, d_ff)\n"
    "        # Router: linear -> softmax over 2 experts\n"
    "        self.router = nn.Linear(d_model, 2, bias=True)\n"
    "\n"
    "    def forward(\n"
    "        self, x: torch.Tensor\n"
    "    ) -> Tuple[torch.Tensor, torch.Tensor]:\n"
    "        \"\"\"Returns (output, routing_probs).\"\"\"\n"
    "        routing_logits = self.router(x)                        # (B, 2)\n"
    "        routing_probs  = F.softmax(routing_logits, dim=-1)     # (B, 2)\n"
    "        expert0_out = self.numeric_expert(x)                   # (B, d_model)\n"
    "        expert1_out = self.semantic_expert(x)                  # (B, d_model)\n"
    "        # Soft routing: weighted combination\n"
    "        output = (routing_probs[:, 0:1] * expert0_out +\n"
    "                  routing_probs[:, 1:2] * expert1_out)\n"
    "        return output + x, routing_probs\n"
    "\n"
    "\n"
    "def generate_typed_tokens(n: int, d: int, rng: torch.Generator) -> Tuple[torch.Tensor, torch.Tensor]:\n"
    "    \"\"\"Generate numeric (type 0) and textual (type 1) tokens with distinct statistics.\"\"\"\n"
    "    types = torch.randint(0, 2, (n,))\n"
    "    tokens = torch.zeros(n, d)\n"
