    "    tokens[numeric_mask] *= (torch.rand(numeric_mask.sum(), d) > 0.7).float()\n"
    "    # Textual: lower variance, dense\n"
    "    tokens[~numeric_mask] = torch.randn((~numeric_mask).sum(), d) * 0.5\n"
    "    return tokens, types\n"
    "\n"
    "\n"
    "D_SPEC = 64\n"
    "N_SPEC = 500\n"
