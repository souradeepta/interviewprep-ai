    "    task_loss.backward()\n"
    "    optimizer_spec.step()\n"
    "\n"
    "# Evaluate routing specialization\n"
    "specialist.eval()\n"
    "with torch.no_grad():\n"
    "    _, routing_final = specialist(X_typed)\n"
    "\n"
    "numeric_to_exp0 = routing_final[token_types == 0, 0].mean().item()\n"
    "textual_to_exp1 = routing_final[token_types == 1, 1].mean().item()\n"
    "\n"
