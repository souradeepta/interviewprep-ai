    "        out, sf, n_skip = block(x_seq, ffn_threshold=thr)\n"
    "    total_tokens = BATCH_T * SEQ_LEN\n"
    "    # FFN is ~2/3 of transformer block compute\n"
    "    flops_saved = sf * 66.7\n"
    "    print(f'{thr:<12.1f} {sf*100:<10.1f} {n_skip:<15d} {flops_saved:<15.1f}')\n"
