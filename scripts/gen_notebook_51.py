    "            nn.Parameter(torch.randn(chunk_in, out_f) * 0.02)\n"
    "            for _ in range(tp_degree)\n"
    "        ])\n"
    "\n"
    "    def forward(self, partial_inputs: List[torch.Tensor]) -> torch.Tensor:\n"
    "        \"\"\"AllReduce: sum partial contributions from each device.\"\"\"\n"
    "        partials = [inp @ shard for inp, shard in zip(partial_inputs, self.weight_shards)]\n"
    "        return torch.stack(partials, dim=0).sum(dim=0)   # AllReduce (sum)\n"
