    "# Real-World Example 3: Fault-Tolerant Inference with Node Failure + Rerouting\n"
    "# Simulates a 2-node cluster where node 0 fails at step=15, rerouted to node 1\n"
    "\n"
    "import time\n"
    "\n"
    "@dataclass\n"
    "class InferenceNode:\n"
    "    node_id: int\n"
    "    healthy: bool = True\n"
    "    requests_served: int = 0\n"
    "    total_latency_ms: float = 0.0\n"
    "\n"
    "    def serve(self, req_id: int, fail_at_step: int, current_step: int) -> float:\n"
    "        \"\"\"Simulate serving one request. Raises RuntimeError if node failed.\"\"\"\n"
    "        if not self.healthy or (self.node_id == 0 and current_step >= fail_at_step):\n"
    "            self.healthy = False\n"
    "            raise RuntimeError(f'Node {self.node_id} timed out / unreachable')\n"
    "        # Simulate variable latency (10-30ms)\n"
    "        latency = np.random.uniform(10, 30)\n"
    "        self.requests_served += 1\n"
    "        self.total_latency_ms += latency\n"
    "        return latency\n"
    "\n"
    "\n"
    "class FaultTolerantCluster:\n"
    "    \"\"\"Routes requests to primary node; falls back to backup on failure.\"\"\"\n"
    "    def __init__(self):\n"
    "        self.primary = InferenceNode(node_id=0)\n"
    "        self.backup  = InferenceNode(node_id=1)\n"
    "        self.events: List[Dict] = []\n"
    "\n"
    "    def route_request(self, req_id: int, fail_at_step: int, current_step: int) -> Dict:\n"
    "        \"\"\"Try primary first; on failure reroute to backup and log recovery.\"\"\"\n"
    "        t_start = time.perf_counter()\n"
    "        try:\n"
    "            latency = self.primary.serve(req_id, fail_at_step, current_step)\n"
    "            event = {'req_id': req_id, 'node': 'primary', 'latency_ms': latency, 'rerouted': False}\n"
