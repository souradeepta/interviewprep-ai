    "# Row-parallel: split W by rows -> each GPU gets partial input, reduce after\n"
    "\n"
    "class ColumnParallelLinear(nn.Module):\n"
    "    \"\"\"First linear in FFN: split output features across TP devices.\"\"\"\n"
    "    def __init__(self, in_f: int, out_f: int, tp_degree: int):\n"
    "        super().__init__()\n"
    "        assert out_f % tp_degree == 0, 'out_features must be divisible by tp_degree'\n"
