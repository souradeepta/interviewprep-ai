    "        self.chunk_size = out_f // tp_degree\n"
    "        # In real TP each device holds only its shard; here we hold all for simulation\n"
    "        self.weight_shards = nn.ParameterList([\n"
    "            nn.Parameter(torch.randn(in_f, self.chunk_size) * 0.02)\n"
    "            for _ in range(tp_degree)\n"
    "        ])\n"
    "\n"
    "    def forward(self, x: torch.Tensor) -> List[torch.Tensor]:\n"
    "        \"\"\"Returns list of partial outputs, one per simulated device.\"\"\"\n"
    "        return [x @ shard for shard in self.weight_shards]\n"
    "\n"
    "\n"
    "class RowParallelLinear(nn.Module):\n"
    "    \"\"\"Second linear in FFN: each device computes partial contribution, then AllReduce.\"\"\"\n"
    "    def __init__(self, in_f: int, out_f: int, tp_degree: int):\n"
